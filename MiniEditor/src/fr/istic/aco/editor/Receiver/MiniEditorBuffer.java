package fr.istic.aco.editor.Receiver;

/**
 * The Interface MiniEditorBuffer.
 * 
 * @author Shashi Mohan Reddy, Nidhi Saini
 */
public interface MiniEditorBuffer {
	
	/**
	 * Read.
	 *
	 * @param begin the begin
	 * @param end the end
	 * @return the string
	 */
	public String Read(int begin,int end );
	
	/**
	 * Write.
	 *
	 * @param beg the beg
	 * @param end the end
	 * @param Str the str
	 */
	public void write(int beg,int end,String Str);
	
	/**
	 * Gets the str.
	 *
	 * @return the str
	 */
	public String getStr();
	
	/**
	 * Sets the str.
	 *
	 * @param str the new str
	 */
	public void setStr(String str);
	
	/**
	 * Gets the length.
	 *
	 * @return the length
	 */
	public int getLength();
}
